abstract class ZodiacUtils {
  static const List<String> allSigns = [
    'aries',
    'taurus',
    'gemini',
    'cancer',
    'leo',
    'virgo',
    'libra',
    'scorpio',
    'sagittarius',
    'capricorn',
    'aquarius',
    'pisces',
  ];

  static const Map<String, String> _emojis = {
    'aries': '♈',
    'taurus': '♉',
    'gemini': '♊',
    'cancer': '♋',
    'leo': '♌',
    'virgo': '♍',
    'libra': '♎',
    'scorpio': '♏',
    'sagittarius': '♐',
    'capricorn': '♑',
    'aquarius': '♒',
    'pisces': '♓',
  };

  /// Returns the lowercase zodiac sign for a given birthdate, or null.
  static String? fromBirthdate(DateTime birthdate) {
    final m = birthdate.month;
    final d = birthdate.day;

    if ((m == 3 && d >= 21) || (m == 4 && d <= 19)) return 'aries';
    if ((m == 4 && d >= 20) || (m == 5 && d <= 20)) return 'taurus';
    if ((m == 5 && d >= 21) || (m == 6 && d <= 20)) return 'gemini';
    if ((m == 6 && d >= 21) || (m == 7 && d <= 22)) return 'cancer';
    if ((m == 7 && d >= 23) || (m == 8 && d <= 22)) return 'leo';
    if ((m == 8 && d >= 23) || (m == 9 && d <= 22)) return 'virgo';
    if ((m == 9 && d >= 23) || (m == 10 && d <= 22)) return 'libra';
    if ((m == 10 && d >= 23) || (m == 11 && d <= 21)) return 'scorpio';
    if ((m == 11 && d >= 22) || (m == 12 && d <= 21)) return 'sagittarius';
    if ((m == 12 && d >= 22) || (m == 1 && d <= 19)) return 'capricorn';
    if ((m == 1 && d >= 20) || (m == 2 && d <= 18)) return 'aquarius';
    if ((m == 2 && d >= 19) || (m == 3 && d <= 20)) return 'pisces';
    return null;
  }

  /// "Aries" — title-cased display name.
  static String displayName(String sign) {
    if (sign.isEmpty) return '';
    return sign[0].toUpperCase() + sign.substring(1);
  }

  /// Returns the Unicode glyph for a sign, e.g. "♈" for aries.
  static String emoji(String sign) => _emojis[sign] ?? '';
}
